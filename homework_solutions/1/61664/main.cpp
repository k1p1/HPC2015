float derCorput(const unsigned n)
{
    unsigned b = 1 << 31;
    float ans = 0;

    char digit = 31;

    while(!(b & n) && b > 0)
    {
        b >>= 1;
        --digit;
    }

    while(b > 0)
    {
        if(b & n)
            ans += reversed[digit];

        b >>= 1;
        --digit;
    }

    return ans;
}

